Student data[346];


void sortByName(Student data[ ], int n) {
    Student y;
    int i;
    for(int k=1; k<n; k++) {
        y=data[k];
        for(i=k-1; i>=0; i--) {
            if(y.name<data[i].name) {
                data[i+1]=data[i];
            } else break;
        }
        data[i+1]=y;
    }
}
int  binarySearchByName(Student data[], int n, string name) {
    int L=0;
    int H =n-1;
    while(L<=H) {
        int m = L+(H-L)/2;
        if(name<data[m].name) {
            H= m-1;
        } else if(name<data[m].name) {
            L= m-1;
        } else {
            return m;
        }
    }
    return -1;
}

